					//-----------------------------------------------------------------------------------------------------------
					case RestRequestResult.ServiceUnavailable : Content = 
						 string.Format ( Content, "SERVICE_UNAVAILABLE" , "none", "block" ); break;
					//-----------------------------------------------------------------------------------------------------------
					}
